    public static boolean isKnownISO639Type(String iso639Code) {
        return getISO639Type(iso639Code) != null;
    }

    public static ISO639Type getISO639Type(String iso639Code) {
        String code = iso639Code.toLowerCase();
        for (ISO639Type iso639Type : ISO639_TYPES) {
            if (iso639Type.getCode().equals(code))
                return iso639Type;
        }
        return null;
    }

    public static boolean isKnownISO639Language(String language) {
        return getISO639Language(language) != null;
    }

    public static ISO639Type getISO639Language(String language) {
        for (ISO639Type iso639Type : ISO639_TYPES) {
            if (iso639Type.getLanguage().equals(language))
                return iso639Type;
        }
        return null;
    }

    private ISO639Type(String language, String code) {
        this.language = language;
        this.code = code;
    }

    public String getCode() {
        return code;
    }

    public String getLanguage() {
        return language;
    }

    private String code;
    private String language;

    public String toString() {
        return "ISO639Type[code=" + getCode() + ", type=" + getLanguage() + "]";
    }
